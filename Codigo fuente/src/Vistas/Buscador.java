    private void txtBuscadorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscadorKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_DOWN && tblProductos.getRowCount() > 0) {
            tblProductos.setRowSelectionInterval(0, 0);
        } else if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (tblProductos.getRowCount() == 1) {
                select(tblProductos.getValueAt(0, 0).toString());
            } else {
                tblProductos.setRowSelectionInterval(0, 0);
            }
        } else {
            cargarDatos(txtBuscador.getText());
        }
    }//GEN-LAST:event_txtBuscadorKeyReleased

