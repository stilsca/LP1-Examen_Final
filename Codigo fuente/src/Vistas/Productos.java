    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int row = tblProductos.getSelectedRow();
        if (row >= 0) {
            String idProducto = tblProductos.getValueAt(row, 0).toString();
            ResultSet pro = session.listar("SELECT p.*,nombreMarca,nombreImpuesto FROM productos p INNER JOIN marcas m ON m.idMarca=p.idMarca INNER JOIN impuestos im ON im.idImpuesto=p.idImpuesto WHERE idProducto=" + idProducto);
            try {
                if (pro.next()) {
                    txtIdProducto.setText(idProducto);
                    txtProducto.setText(pro.getString("nombreProducto"));
                    txtCodigo.setText(pro.getString("codigo"));
                    txtExistencia.setText(pro.getInt("existencia") + "");
                    txtPrecio.setText(pro.getInt("precio") + "");
                    cmbImpuestos.setSelectedItem(pro.getString("nombreImpuesto"));
                    cmbMarca.setSelectedItem(pro.getString("nombreMarca"));
                    txtIdProducto.setEditable(false);
                    txtProducto.requestFocus();
                    txtProducto.selectAll();
                    activar(false);
                }
            } catch (SQLException ex) {

            }

        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un registro para editar");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int row = tblProductos.getSelectedRow();
        if (row >= 0) {
            String idProducto = tblProductos.getValueAt(row, 0).toString();
            int op = JOptionPane.showConfirmDialog(this, "Eliminar el registro", "Eliminar", JOptionPane.YES_NO_OPTION);
            if (op == JOptionPane.YES_OPTION) {

                ResultSet uso = session.getCon().listar("SELECT * FROM ventas_det WHERE idProducto=" + idProducto);
                try {
                    if (!uso.next()) {
                        session.getCon().ejecutar("DELETE FROM productos WHERE idMarca=" + idProducto);
                    } else {
                        JOptionPane.showMessageDialog(this, "El registro esta en uso, no es posible eliminar", "Error", JOptionPane.WARNING_MESSAGE);
                        return;
                    }
                } catch (SQLException ex) {

                }
                cargarDatos("");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un registro para eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

