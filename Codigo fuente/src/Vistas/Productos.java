    private void cargarDatos(String val) {
        String sentencia = "SELECT * FROM productos WHERE nombreProducto LIKE '%" + val + "%'";
        ResultSet datos = session.getCon().listar(sentencia);
        String titulos[] = {"Id", "Descripción", "Existencia", "Precio"};
        DefaultTableModel m;
        m = new DefaultTableModel(null, titulos) {
            @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false
                return false;
            }
        };
        String fila[] = new String[4];
        try {
            while (datos.next()) {
                fila[0] = datos.getString("idProducto");
                fila[1] = datos.getString("nombreProducto");
                fila[2] = datos.getInt("existencia")+"";
                fila[3] = datos.getInt("precio")+"";
                m.addRow(fila);
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
        tblProductos.setModel(m);
    }

