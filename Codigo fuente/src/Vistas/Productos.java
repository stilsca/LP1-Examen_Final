    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String idProducto = txtIdProducto.getText().trim();
        String nombreProducto = txtProducto.getText().trim();
        String codigo = txtCodigo.getText().trim();
        String precio = txtPrecio.getText().trim();
        int pre = 0;
        int exi = 0;
        try {
            pre = Integer.parseInt(precio.replace(".", ""));
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Precio no valido");
            return;
        }
        String existencia = txtExistencia.getText().trim();
        try {
            exi = Integer.parseInt(existencia.replace(".", ""));
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Existencia no valida");
            return;
        }
        if (nombreProducto.length() > 0) {
            int idMarca = idMarcas.get(cmbMarca.getSelectedIndex());
            int idImpuesto = idImpuestos.get(cmbImpuestos.getSelectedIndex());
            if (idProducto.length() > 0) {
                session.ejecutar("UPDATE productos SET nombreProducto='" + nombreProducto + "',codigo='" + codigo + "',precio='" + pre + "',existencia='" + exi + "',idMarca=" + idMarca + ",idImpuesto="+idImpuesto+" WHERE idProducto=" + idProducto);
            } else {
                session.ejecutar("INSERT INTO productos (nombreProducto,codigo,precio,existencia,idMarca,idImpuesto) VALUES ('" + nombreProducto + "','" + codigo + "','" + pre + "','" + exi + "'," + idMarca + ","+idImpuesto+")");
            }
            cargarDatos("");
            limpiar();
            activar(true);

        } else {
            JOptionPane.showMessageDialog(this, "Ingrese descripción de la marca");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

