    public void cargarMarcas() {
        idMarcas = new ArrayList();

        ResultSet esta = session.getCon().listar("SELECT * FROM marcas");
        Vector<String> cus = new Vector();
        try {
            while (esta.next()) {
                cus.add(esta.getString("nombreMarca"));
                idMarcas.add(esta.getInt("idMarca"));
            }
        } catch (SQLException ex) {
            //Log.log(ex, this);
        }
        DefaultComboBoxModel mdl = new DefaultComboBoxModel(cus);
        cmbMarca.setModel(mdl);
    }
    
